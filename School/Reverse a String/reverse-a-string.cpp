class Solution {
  public:
    string revStr(string S) {
        // code here
        stack<char>st;
        for(int i=0;i<S.length();i++){
            st.push(S[i]);
        }
        string ans;
        while(!st.empty()){
            ans+=st.top();
            st.pop();
        }
        return ans;
    }
};

